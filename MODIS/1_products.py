# The MOD13Q1 product is a "MODIS/Terra Vegetation Indices 16-Day L3 Global 250m SIN Grid V006" product, 
# which means that the data is provided at a spatial resolution of 250 meters.

# This product provides the Normalized Difference Vegetation Index (NDVI) and the 
# Enhanced Vegetation Index (EVI) at a 16-day temporal resolution. These indices 
# are widely used to monitor vegetation cover and health. NDVI is calculated from 
# the red and near-infrared bands of the MODIS sensor, while EVI is calculated using 
# the blue, red, and near-infrared bands of the sensor. The NDVI and EVI values range 
# from -1 to 1, with higher values indicating more vegetation.

# NDVI and EVI are widely used as indicators of vegetation health, productivity and 
# water content, they are widely used in agriculture, ecology, and remote sensing studies.


